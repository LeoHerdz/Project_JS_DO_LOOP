/* Question 4: Sum of Numbers 1 to 20
Write a do-while loop to calculate and print the sum of numbers from 1 to 20.
Expected Output: The sum should be 210 */

 let i = 1 
let sum = 0
do {
    sum = sum + i; 
   
    i ++;
    
} while (i <=20);
console.log("The final total of 1-20 is "+  sum ); 
 
